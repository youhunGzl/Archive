package com.archive.utility;

public class tCatalogUtil {
	private String catalog_no;
	private int count;
	private int file;
	private int material;
	private int multimedia;
	
	
	public String getCatalog_no() {
		return catalog_no;
	}
	public void setCatalog_no(String catalog_no) {
		this.catalog_no = catalog_no;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getFile() {
		return file;
	}
	public void setFile(int file) {
		this.file = file;
	}
	public int getMaterial() {
		return material;
	}
	public void setMaterial(int material) {
		this.material = material;
	}
	public int getMultimedia() {
		return multimedia;
	}
	public void setMultimedia(int multimedia) {
		this.multimedia = multimedia;
	}
	
	

}
